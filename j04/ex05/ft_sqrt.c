int	ft_sqrt(int nb)
{
	int a;

	a = 1;
	if (nb < 0)
		return (0);
	while ((a * a) != nb)
	{
		if ((a * a) > nb)
			return (0);
		a = a + 1;
	}
	nb = nb / a;
	return (nb);
}
